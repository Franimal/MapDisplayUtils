package tim.mappingutils.types;

/**
 * A ScreenCoordinate represents a point on a screen.
 * 
 * @author Tim
 *
 */
public class ScreenCoordinate {

	public final double x;
	public final double y;
	public final double z;
	
	public ScreenCoordinate(double x, double y, double z){
		this.x=x;
		this.y=y;
		this.z=z;
	}
	
}
